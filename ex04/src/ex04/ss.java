package ex04;

public class ss {
	
	
public static void main(String[] args) {
	
	   boolean a = true;
	   boolean b = !a;
	   // !은 부정 (a는 부정한다는 뜻 (a는 참이므로 부정한다면))
	   
	   
	   System.out.println("a = "+a);
	   System.out.println("b = "+b);
	   
	   int c = 10;
	   int d = 20;
	   
	   System.out.println("c = "+c);
	   System.out.println("d = "+d);
	   
	   // d= d+c; 와 동일 (간단하게 줄임)
	   d += c;
	   
	   
	   System.out.println("d = "+ d);
	   
	   
	   



}

}
